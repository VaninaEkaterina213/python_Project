n=15 #всего чисел
print("Введите 15 чисел ")
a = [int(input())
     for j in range(n)]# массив,который заполняется с клавиатуры
sum1 = sum2 = 0 # пока пустые суммы 2-х случаев
i = 0 #счётчик
while i < n:
    if a[i] > 0:# проверка положительных чисел
        sum1 += a[i]
    else: # иначе: отрицательных
        sum2 += a[i]
    i += 1
print('Сумма положительных:', sum1, 'Сумма отрицательных:', sum2, sep='\n')#вывод значений с переносом


